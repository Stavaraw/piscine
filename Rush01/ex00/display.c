#include <unistd.h>

void display(int *array)
{
	int i;

	i = 0;
	while (i < 16)
	{	array[i] += 48;
		write(1, &array[i], 1);
		if (i % 4 == 3)
			write(1, "\n", 1);
		i++;
	}
}
