#include "include.h"

int	ft_check_number(char *nbr)
{
	while (*nbr)
	{
		if (!(*nbr >= '0' && *nbr <= '9'))
		{
			write(1, "Error\n", 6);
			return (0);
		}
		nbr++;
	}
	return (1);
}
