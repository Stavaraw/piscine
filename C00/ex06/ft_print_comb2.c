#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a;
	int		b;
	char	x;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			x = a / 10 + 48;
			write(1, &x, 1);
			x = a % 10 + 48;
			write(1, &x, 1);
			x = b / 10 + 48;
			write(1, " ", 1);
			write(1, &x, 1);
			x = b % 10 + 48;
			write(1, &x, 1);
			if (a != 98)
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
